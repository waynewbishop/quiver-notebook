/* --- Overflow menu --- */

function setupOverflowMenu() {
    const btn = document.getElementById('menu-btn');
    const dropdown = document.getElementById('menu-dropdown');
    if (!btn || !dropdown) return;

    const close = () => {
        dropdown.hidden = true;
        btn.setAttribute('aria-expanded', 'false');
        // Also fold any open submenu so it doesn't reappear the next time
        // the parent dropdown opens.
        const subPanel = dropdown.querySelector('.menu-submenu-panel');
        const subTrigger = dropdown.querySelector('.menu-submenu-trigger');
        if (subPanel) subPanel.hidden = true;
        if (subTrigger) subTrigger.setAttribute('aria-expanded', 'false');
    };

    btn.addEventListener('click', (e) => {
        e.stopPropagation();
        const open = dropdown.hidden;
        dropdown.hidden = !open;
        btn.setAttribute('aria-expanded', String(open));
    });

    // Clicking anywhere else closes the menu
    document.addEventListener('click', (e) => {
        if (dropdown.hidden) return;
        if (!dropdown.contains(e.target) && e.target !== btn) close();
    });

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && !dropdown.hidden) close();
    });

    // Wire each menu item to its handler, then close the menu
    const wire = (id, handler) => {
        const el = document.getElementById(id);
        if (!el) return;
        el.addEventListener('click', () => {
            close();
            handler();
        });
    };

    wire('menu-download', downloadCurrentCode);
    wire('menu-upload', () => {
        const input = document.getElementById('file-upload-input');
        if (input) input.click();
    });
    wire('menu-rename', beginRename);
    wire('menu-toggle-line-numbers', toggleLineNumbers);
    wire('menu-quiver-docs', () => {});
    wire('menu-shortcuts', showShortcuts);

    setupFontSizeSubmenu(close);
}

/* --- Download .swift file --- */

function downloadCurrentCode() {
    if (!editor) return;
    const code = editor.getValue();
    const filename = suggestFilename();
    const blob = new Blob([code], { type: 'text/plain;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
    setStatus(`Downloaded ${filename}`, 'ok');
}

function suggestFilename() {
    const title = currentExampleTitle && currentExampleTitle !== 'Untitled'
        ? currentExampleTitle.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '')
        : 'quiver-notebook';
    return (title || 'quiver-notebook') + '.swift';
}

/* --- Upload .swift file --- */

function setupFileUpload() {
    const input = document.getElementById('file-upload-input');
    if (!input) return;

    input.addEventListener('change', (e) => {
        const file = e.target.files && e.target.files[0];
        if (!file) return;
        readAndLoadFile(file);
        input.value = ''; // reset so selecting the same file twice re-triggers
    });
}

function readAndLoadFile(file) {
    if (!editor) return;
    const reader = new FileReader();
    reader.onload = () => {
        const text = String(reader.result || '');
        const current = editor.getValue().trim();
        if (current.length > 0 && current !== loadedExampleCode) {
            const ok = confirm(`Replace current code with contents of ${file.name}?`);
            if (!ok) return;
        }
        editor.setValue(text);
        localStorage.setItem(STORAGE_KEY, text);
        if (activeExample) {
            activeExample.classList.remove('active');
            activeExample = null;
        }
        const title = file.name.replace(/\.swift$/i, '');
        currentExampleTitle = title;
        loadedExampleCode = text;
        setBreadcrumbSource('Opened');
        setBreadcrumbTitle(title);
        setBreadcrumbModified(false);
        setStatus(`Opened ${file.name}`, 'ok');
    };
    reader.onerror = () => setStatus('Failed to read file.', 'error');
    reader.readAsText(file);
}

/* --- Drag and drop ---*/

function setupDragDrop() {
    const pane = document.getElementById('editor-pane');
    const zone = document.getElementById('drop-zone');
    if (!pane || !zone) return;

    let dragDepth = 0;

    pane.addEventListener('dragenter', (e) => {
        if (!hasFiles(e)) return;
        e.preventDefault();
        dragDepth += 1;
        zone.hidden = false;
    });

    pane.addEventListener('dragover', (e) => {
        if (!hasFiles(e)) return;
        e.preventDefault();
        e.dataTransfer.dropEffect = 'copy';
    });

    pane.addEventListener('dragleave', (e) => {
        if (!hasFiles(e)) return;
        dragDepth -= 1;
        if (dragDepth <= 0) {
            dragDepth = 0;
            zone.hidden = true;
        }
    });

    pane.addEventListener('drop', (e) => {
        if (!hasFiles(e)) return;
        e.preventDefault();
        dragDepth = 0;
        zone.hidden = true;
        const file = e.dataTransfer.files && e.dataTransfer.files[0];
        if (!file) return;
        if (!/\.swift$/i.test(file.name)) {
            setStatus('Only .swift files can be opened.', 'error');
            return;
        }
        readAndLoadFile(file);
    });
}

function hasFiles(e) {
    return e.dataTransfer && Array.from(e.dataTransfer.types || []).includes('Files');
}

