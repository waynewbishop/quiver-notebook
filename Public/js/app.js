/* --- Rename notebook ---
   The breadcrumb title is the canonical "what file am I editing?" cue, so we
   make it the primary rename surface. Click (or press Enter/F2 when focused)
   replaces the span with an input; Enter commits, Esc cancels, blur commits.
   Renaming an example forks it into a user notebook: source flips from
   'Examples' to 'Notebook' and the active-example highlight clears so the
   student understands they've branched off and the original template is
   still intact. */

let renameActive = false;

function beginRename() {
    if (!editor || renameActive) return;
    const span = document.getElementById('breadcrumb-current');
    if (!span) return;
    renameActive = true;

    const originalTitle = currentExampleTitle || 'Untitled';
    const input = document.createElement('input');
    input.type = 'text';
    input.className = 'breadcrumb-rename-input';
    input.value = originalTitle === 'Untitled' ? '' : originalTitle;
    input.placeholder = 'Notebook name';
    input.setAttribute('aria-label', 'Notebook name');
    input.maxLength = 120;

    span.replaceWith(input);
    input.focus();
    input.select();

    let finished = false;
    const commit = () => {
        if (finished) return;
        finished = true;
        const cleaned = sanitizeTitle(input.value);
        const newTitle = cleaned || originalTitle || 'Untitled';
        finishRename(input, newTitle, newTitle !== originalTitle);
    };
    const cancel = () => {
        if (finished) return;
        finished = true;
        finishRename(input, originalTitle, false);
    };

    input.addEventListener('keydown', (e) => {
        if (e.key === 'Enter') { e.preventDefault(); commit(); }
        else if (e.key === 'Escape') { e.preventDefault(); cancel(); }
    });
    input.addEventListener('blur', commit);
}

function finishRename(input, newTitle, didChange) {
    // Restore the span element in the exact DOM position so wiring stays intact.
    const span = document.createElement('span');
    span.id = 'breadcrumb-current';
    span.className = 'breadcrumb-current';
    span.setAttribute('role', 'button');
    span.setAttribute('tabindex', '0');
    span.setAttribute('title', 'Click to rename (Enter)');
    span.textContent = newTitle;
    input.replaceWith(span);
    wireBreadcrumbRename(span);

    currentExampleTitle = newTitle;
    setBreadcrumbTitle(newTitle);
    renameActive = false;

    if (!didChange) return;

    // Fork-from-example rule: if the user just renamed an example, clear the
    // sidebar highlight and flip the source so they see they're now working
    // on their own notebook. The original example on disk is untouched.
    const sourceEl = document.getElementById('breadcrumb-source');
    const source = sourceEl ? sourceEl.textContent : '';
    if (source === 'Examples') {
        if (activeExample) {
            activeExample.classList.remove('active');
            activeExample = null;
        }
        loadedExampleCode = null;
        setBreadcrumbSource('Notebook');
        setBreadcrumbModified(false);
        setStatus(`Forked to “${newTitle}”`, 'ok');
    } else {
        setStatus(`Renamed to “${newTitle}”`, 'ok');
    }
}

// Strip path separators and trailing .swift, collapse whitespace, trim.
// Display name stays human-friendly; download filename sanitation happens
// separately in suggestFilename().
function sanitizeTitle(raw) {
    if (!raw) return '';
    let t = String(raw).replace(/[\\/]+/g, ' ').replace(/\.swift$/i, '');
    t = t.replace(/\s+/g, ' ').trim();
    return t;
}

function wireBreadcrumbRename(span) {
    if (!span) return;
    span.addEventListener('click', beginRename);
    span.addEventListener('keydown', (e) => {
        if (e.key === 'Enter' || e.key === 'F2') {
            e.preventDefault();
            beginRename();
        }
    });
}

