async function runCode() {
    if (!editor) return;
    const code = editor.getValue();
    const runBtn = document.getElementById('run-btn');

    runBtn.disabled = true;
    runBtn.classList.add('running');
    setStatus('Running...', 'running');
    setKernelStatus('busy', 'Running');
    setDurationPill(null);
    setOutput('', '');

    try {
        const res = await fetch('/api/run', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ code })
        });

        if (!res.ok) {
            throw new Error(`HTTP ${res.status}`);
        }

        const result = await res.json();
        setOutput(result.stdout || '', result.stderr || '');

        if (result.exitCode === 0) {
            setStatus(`Wall time: ${result.durationMs} ms`, 'ok');
            setKernelStatus('ready', 'Ready');
            setDurationPill(`${result.durationMs} ms`, 'ok');
        } else {
            setStatus(`Exit ${result.exitCode} · Wall time: ${result.durationMs} ms`, 'error');
            setKernelStatus('ready', 'Ready');
            setDurationPill(`exit ${result.exitCode} · ${result.durationMs} ms`, 'error');
        }
    } catch (err) {
        setOutput('', `Request failed: ${err.message}`);
        setStatus('Failed.', 'error');
        setKernelStatus('error', 'Error');
        setDurationPill('failed', 'error');
    } finally {
        runBtn.disabled = false;
        runBtn.classList.remove('running');
    }
}

