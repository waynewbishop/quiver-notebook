/* --- Font size (presenter mode) --- */

function applyFontSize(name) {
    if (!FONT_SIZES[name]) return;
    fontSize = name;
    if (editor) editor.updateOptions({ fontSize: FONT_SIZES[name].editor });
    document.documentElement.setAttribute('data-font-size', name);
    try { localStorage.setItem(FONT_SIZE_KEY, name); } catch (e) {}
    updateFontSizeMenuLabel();
    setStatus(`Font size: ${FONT_SIZES[name].label}`, 'ok');
}

// Cycles forward through the presets; wraps from presenter back to normal so
// the menu item keeps being useful after the third click.
function stepFontSize(delta) {
    const i = FONT_SIZE_ORDER.indexOf(fontSize);
    const len = FONT_SIZE_ORDER.length;
    const next = ((i + delta) % len + len) % len;
    applyFontSize(FONT_SIZE_ORDER[next]);
}

function updateFontSizeMenuLabel() {
    // Reflect the active preset on the radio items in the submenu. Name kept
    // for compatibility with the boot-time call in the Monaco init block.
    const options = document.querySelectorAll('.menu-font-size-option');
    options.forEach((el) => {
        const active = el.getAttribute('data-size') === fontSize;
        el.setAttribute('aria-checked', active ? 'true' : 'false');
        el.classList.toggle('is-active', active);
    });
}

function setupFontSizeSubmenu(closeParent) {
    const wrap = document.getElementById('menu-font-size');
    if (!wrap) return;
    const trigger = wrap.querySelector('.menu-submenu-trigger');
    const panel = wrap.querySelector('.menu-submenu-panel');
    if (!trigger || !panel) return;

    const openSub = () => {
        panel.hidden = false;
        trigger.setAttribute('aria-expanded', 'true');
    };
    const closeSub = () => {
        panel.hidden = true;
        trigger.setAttribute('aria-expanded', 'false');
    };

    trigger.addEventListener('click', (e) => {
        e.stopPropagation(); // don't let the parent dropdown's outside-click close everything
        if (panel.hidden) openSub(); else closeSub();
    });

    panel.querySelectorAll('.menu-font-size-option').forEach((opt) => {
        opt.addEventListener('click', (e) => {
            e.stopPropagation();
            const size = opt.getAttribute('data-size');
            applyFontSize(size);
            closeSub();
            if (typeof closeParent === 'function') closeParent();
        });
    });
}

