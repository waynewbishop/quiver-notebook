/* --- Global keyboard shortcuts (outside Monaco) --- */

function setupGlobalShortcuts() {
    document.addEventListener('keydown', (e) => {
        // Don't hijack keys while typing in an input or the editor
        const target = e.target;
        const inInput = target && (target.tagName === 'INPUT' || target.tagName === 'TEXTAREA');
        const inEditor = target && target.closest && target.closest('.monaco-host');

        // Escape: close cheatsheet
        if (e.key === 'Escape') {
            const overlay = document.getElementById('shortcuts-overlay');
            if (overlay && !overlay.hidden) { hideShortcuts(); return; }
        }

        // Cmd/Ctrl+B: toggle sidebar. Fires everywhere except inside inputs;
        // when focus is inside the Monaco editor, the command registered via
        // editor.addCommand handles it and the event never reaches here.
        if ((e.metaKey || e.ctrlKey) && !e.shiftKey && !e.altKey && (e.key === 'b' || e.key === 'B')) {
            if (inInput) return;
            e.preventDefault();
            toggleSidebar();
            return;
        }

        // Cmd/Ctrl + =/-/0: presenter font sizing. Same suppression rules;
        // Monaco's own addCommand handles these when focus is in the editor.
        if ((e.metaKey || e.ctrlKey) && !e.shiftKey && !e.altKey && !inInput) {
            if (e.key === '=' || e.key === '+') { e.preventDefault(); stepFontSize(+1); return; }
            if (e.key === '-')                  { e.preventDefault(); stepFontSize(-1); return; }
            if (e.key === '0')                  { e.preventDefault(); applyFontSize('normal'); return; }
        }

        // ? only fires when not typing in a field or in the editor
        if (inInput || inEditor) return;

        if (e.key === '?' || (e.shiftKey && e.key === '/')) {
            e.preventDefault();
            showShortcuts();
        }
    });
}

