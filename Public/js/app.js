/* --- Run endpoint --- */

async function runCode() {
    if (!editor) return;
    const code = editor.getValue();
    const runBtn = document.getElementById('run-btn');

    runBtn.disabled = true;
    runBtn.classList.add('running');
    setStatus('Running...', 'running');
    setKernelStatus('busy', 'Running');
    setDurationPill(null);
    setOutput('', '');

    try {
        const res = await fetch('/api/run', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ code })
        });

        if (!res.ok) {
            throw new Error(`HTTP ${res.status}`);
        }

        const result = await res.json();
        setOutput(result.stdout || '', result.stderr || '');

        if (result.exitCode === 0) {
            setStatus(`Wall time: ${result.durationMs} ms`, 'ok');
            setKernelStatus('ready', 'Ready');
            setDurationPill(`${result.durationMs} ms`, 'ok');
        } else {
            setStatus(`Exit ${result.exitCode} · Wall time: ${result.durationMs} ms`, 'error');
            setKernelStatus('ready', 'Ready');
            setDurationPill(`exit ${result.exitCode} · ${result.durationMs} ms`, 'error');
        }
    } catch (err) {
        setOutput('', `Request failed: ${err.message}`);
        setStatus('Failed.', 'error');
        setKernelStatus('error', 'Error');
        setDurationPill('failed', 'error');
    } finally {
        runBtn.disabled = false;
        runBtn.classList.remove('running');
    }
}

function setDurationPill(text, kind = '') {
    const el = document.getElementById('duration-pill');
    if (!el) return;
    if (text === null || text === '') {
        el.hidden = true;
        el.textContent = '';
        el.className = 'duration-pill';
        return;
    }
    el.hidden = false;
    el.textContent = text;
    el.className = 'duration-pill' + (kind ? ' ' + kind : '');
}

/* --- Examples sidebar --- */

async function loadExamples() {
    const list = document.getElementById('examples-list');
    try {
        const res = await fetch('/api/examples');
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        const examples = await res.json();

        if (examples.length === 0) {
            list.innerHTML = '<li class="examples-loading">No examples found.</li>';
            return;
        }

        list.innerHTML = '';
        examples.forEach(ex => {
            const li = document.createElement('li');
            li.textContent = ex.title;
            li.dataset.name = ex.name;
            li.dataset.title = ex.title.toLowerCase();
            li.addEventListener('click', () => loadExample(ex.name, li));
            list.appendChild(li);
        });
    } catch (err) {
        list.innerHTML = `<li class="examples-loading">Failed to load examples.</li>`;
    }
}

async function loadExample(name, element) {
    if (!editor) return;

    const current = editor.getValue();
    if (current.trim() && current !== loadedExampleCode) {
        const ok = confirm('Replace the current code with this example?');
        if (!ok) return;
    }

    try {
        const res = await fetch(`/api/examples/${encodeURIComponent(name)}`);
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        const detail = await res.json();

        editor.setValue(detail.code);
        localStorage.setItem(STORAGE_KEY, detail.code);

        if (activeExample) activeExample.classList.remove('active');
        element.classList.add('active');
        activeExample = element;

        currentExampleTitle = detail.title;
        loadedExampleCode = detail.code;
        setBreadcrumbSource('Examples');
        setBreadcrumbTitle(detail.title);
        setBreadcrumbModified(false);

        setStatus(`Loaded: ${detail.title}`, 'ok');
    } catch (err) {
        setStatus(`Failed to load example.`, 'error');
    }
}

