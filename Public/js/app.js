/* Quiver Notebook — frontend controller. */

const STORAGE_KEY = 'quiver-notebook-draft';
const THEME_KEY = 'quiver-notebook-theme';
const SIDEBAR_WIDTH_KEY = 'quiver-notebook-sidebar-width';
const SIDEBAR_COLLAPSED_KEY = 'quiver-notebook-sidebar-collapsed';
const OUTPUT_HEIGHT_KEY = 'quiver-notebook-output-height';
const LINE_NUMBERS_KEY = 'quiver-notebook-line-numbers';
const FONT_SIZE_KEY = 'quiver-notebook-font-size';
const TITLE_KEY = 'quiver-notebook-title';
const SOURCE_KEY = 'quiver-notebook-source';

let lineNumbersEnabled = true;

// Three presets for presenter-friendly sizing. Editor size drives Monaco;
// output size drives the .output pane via a data-attribute on <html>.
const FONT_SIZES = {
    normal:    { editor: 13, label: 'Normal' },
    large:     { editor: 18, label: 'Large' },
    presenter: { editor: 24, label: 'Presenter' },
};
const FONT_SIZE_ORDER = ['normal', 'large', 'presenter'];
let fontSize = 'normal';

let editor = null;
let activeExample = null;
let currentExampleTitle = 'Untitled';
let loadedExampleCode = null;

/* --- Quiver docs (Monaco hover + Option-click popup) ---
 * Flat map of symbol name -> { signature, summary } generated by
 * Scripts/generate-docs.swift from Quiver's symbol graph. Loaded once at
 * startup; the hover provider and the Option-click popup both read from
 * this dictionary. The fetch is exposed as a promise (quiverDocsReady) so
 * the Monaco init can await it — otherwise hovers fired before the fetch
 * resolves see an empty map and return null, which looks like "nothing
 * works." If the fetch fails we still resolve (with an empty object) so
 * the editor boots; the hover just returns null, same as a cache miss.
 */
let quiverDocs = {};
let quiverDocsEmptyWarned = false;
const quiverDocsReady = fetch('/quiver-docs.json')
    .then(r => r.ok ? r.json() : {})
    .then(d => {
        quiverDocs = d || {};
        const count = Object.keys(quiverDocs).length;
        console.info('[Quiver] Loaded ' + count + ' doc entries');
        return quiverDocs;
    })
    .catch((err) => {
        console.warn('[Quiver] Failed to load quiver-docs.json:', err);
        quiverDocs = {};
        return quiverDocs;
    });

/* --- Monaco bootstrap --- */

require.config({ paths: { vs: 'https://cdnjs.cloudflare.com/ajax/libs/monaco-editor/0.45.0/min/vs' } });

require(['vs/editor/editor.main'], () => {
    const saved = localStorage.getItem(STORAGE_KEY);
    const initialValue = saved ?? '';

    // Restore line numbers preference
    try {
        const stored = localStorage.getItem(LINE_NUMBERS_KEY);
        if (stored === 'off') lineNumbersEnabled = false;
    } catch (e) {}

    // Restore font size preference
    try {
        const stored = localStorage.getItem(FONT_SIZE_KEY);
        if (stored && FONT_SIZES[stored]) fontSize = stored;
    } catch (e) {}
    document.documentElement.setAttribute('data-font-size', fontSize);

    // Restore notebook title + source label. Only restored when there's a
    // saved draft — a fresh boot with no code stays at the default breadcrumb.
    try {
        if (saved && saved.length > 0) {
            const storedTitle = localStorage.getItem(TITLE_KEY);
            const storedSource = localStorage.getItem(SOURCE_KEY);
            if (storedTitle) {
                currentExampleTitle = storedTitle;
                setBreadcrumbTitle(storedTitle);
            }
            if (storedSource) setBreadcrumbSource(storedSource);
        }
    } catch (e) {}

    // Syntax highlighting palette that matches the Swift Algorithms book
    // (see swift-algorithms/assets/css/style.scss, .highlight rules). The
    // notebook is a companion surface to the book — keeping the same colors
    // for keywords, types, strings, and numbers means students see one
    // pedagogical palette across reading and coding. The dark variant
    // preserves hue relationships while passing contrast on #1e1e1e.
    monaco.editor.defineTheme('quiver-light', {
        base: 'vs',
        inherit: true,
        rules: [
            { token: 'keyword',    foreground: 'ad3da4' },
            { token: 'type',       foreground: '1d1d1f' },
            { token: 'type.identifier', foreground: '1d1d1f' },
            { token: 'string',     foreground: 'd12f1b' },
            { token: 'comment',    foreground: '6C7986' },
            { token: 'identifier', foreground: '1d1d1f' },
            // Numbers and delimiters (brackets, parens, braces, commas, dots)
            // render as neutral grey — primary content that shouldn't compete
            // with the colored categories.
            { token: 'number',     foreground: '5a6270' },
            { token: 'delimiter',  foreground: '5a6270' },
        ],
        colors: {
            'editor.background': '#f7f7f9',
        },
    });
    monaco.editor.defineTheme('quiver-dark', {
        base: 'vs-dark',
        inherit: true,
        rules: [
            { token: 'keyword',    foreground: 'd16fd0' },
            { token: 'type',       foreground: 'e6e6e6' },
            { token: 'type.identifier', foreground: 'e6e6e6' },
            { token: 'string',     foreground: 'ff7d6a' },
            { token: 'comment',    foreground: '8a96a6' },
            { token: 'identifier', foreground: 'e6e6e6' },
            { token: 'number',     foreground: 'c6c9cf' },
            { token: 'delimiter',  foreground: 'c6c9cf' },
        ],
        colors: {
            'editor.background': '#23232b',
        },
    });

    editor = monaco.editor.create(document.getElementById('monaco-editor'), {
        value: initialValue,
        language: 'swift',
        theme: currentMonacoTheme(),
        fontFamily: 'ui-monospace, SFMono-Regular, "SF Mono", Menlo, Consolas, monospace',
        fontSize: FONT_SIZES[fontSize].editor,
        lineNumbers: lineNumbersEnabled ? 'on' : 'off',
        minimap: { enabled: false },
        scrollBeyondLastLine: false,
        automaticLayout: true,
        tabSize: 4,
        insertSpaces: true,
        renderLineHighlight: 'gutter',
        smoothScrolling: true,
        // Make hover explicitly enabled — Monaco's default is on, but some
        // theme/global configurations can silently disable it. Being explicit
        // means the Quiver hover provider is guaranteed a surface to render.
        hover: { enabled: true, delay: 300, sticky: true }
    });

    // Dot-access member highlighting. Monaco's Swift tokenizer treats every
    // named thing as `identifier`, with no distinction between a variable use
    // and a method call. To color method/property accesses separately (e.g.
    // `.mean()`, `.count`, `.predict(...)`), we run a simple pattern pass on
    // every content change and apply a decoration — the word immediately
    // after a `.` gets a CSS class that styles it as a muted purple.
    let memberDecorations = editor.createDecorationsCollection([]);
    const memberRegex = /\.([A-Za-z_][A-Za-z0-9_]*)/g;
    function refreshMemberDecorations() {
        const model = editor.getModel();
        if (!model) return;
        const newDecos = [];
        const lineCount = model.getLineCount();
        for (let line = 1; line <= lineCount; line++) {
            const text = model.getLineContent(line);
            let m;
            memberRegex.lastIndex = 0;
            while ((m = memberRegex.exec(text)) !== null) {
                // `m.index` points at the dot; the name starts one column later.
                const startCol = m.index + 2; // 1-indexed, +1 for dot
                const endCol = startCol + m[1].length;
                newDecos.push({
                    range: new monaco.Range(line, startCol, line, endCol),
                    options: { inlineClassName: 'quiver-member' },
                });
            }
        }
        memberDecorations.set(newDecos);
    }
    refreshMemberDecorations();

    // Sync the menu label with the initial state
    updateLineNumbersMenuLabel();
    updateFontSizeMenuLabel();

    // Debounced auto-save to localStorage
    let saveTimer = null;
    editor.onDidChangeModelContent(() => {
        setSaveIndicator('saving');
        if (saveTimer) clearTimeout(saveTimer);
        saveTimer = setTimeout(() => {
            localStorage.setItem(STORAGE_KEY, editor.getValue());
            markSavedNow();
        }, 400);
        updateBreadcrumbModifiedFlag();
        refreshMemberDecorations();
    });

    // Track cursor position for footer display
    editor.onDidChangeCursorPosition((ev) => {
        updateCursorPosition(ev.position.lineNumber, ev.position.column);
    });

    // Cmd/Ctrl+Enter to run
    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.Enter, () => {
        runCode();
    });

    // Shift+Enter to run (Jupyter muscle memory)
    editor.addCommand(monaco.KeyMod.Shift | monaco.KeyCode.Enter, () => {
        runCode();
    });

    // Cmd/Ctrl+B to toggle the examples sidebar (VS Code convention)
    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.KeyB, () => {
        toggleSidebar();
    });

    // Presenter-mode font sizing (browser/Xcode muscle memory)
    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.Equal,  () => stepFontSize(+1));
    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.Minus,  () => stepFontSize(-1));
    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.Digit0, () => applyFontSize('normal'));

    // Quiver docs hover — tooltip shows method signature and first paragraph
    // of the DocC comment for any symbol present in quiverDocs. Lookup is by
    // the unqualified word under the cursor (Monaco's getWordAtPosition).
    // Overloads collapse to one entry today; see Scripts/generate-docs.swift.
    // Registration waits on quiverDocsReady so the map is populated before
    // the user's first hover fires — otherwise the provider silently returns
    // null during the fetch window and the tooltip never appears.
    // TODO: show every overload in the tooltip once we track them separately.
    quiverDocsReady.then(() => {
        monaco.languages.registerHoverProvider('swift', {
            provideHover(model, position) {
                const word = model.getWordAtPosition(position);
                if (!word) return null;
                if (Object.keys(quiverDocs).length === 0) {
                    if (!quiverDocsEmptyWarned) {
                        console.warn('[Quiver] Hover fired but quiverDocs is empty — fetch may have failed.');
                        quiverDocsEmptyWarned = true;
                    }
                    return null;
                }
                const doc = quiverDocs[word.word];
                console.debug('[Quiver] Hover:', word.word, !!doc);
                if (!doc) return null;

                const contents = [];
                if (doc.signature) contents.push({ value: '```swift\n' + doc.signature + '\n```' });
                if (doc.summary) contents.push({ value: doc.summary });
                if (contents.length === 0) return null;

                return {
                    range: new monaco.Range(
                        position.lineNumber, word.startColumn,
                        position.lineNumber, word.endColumn
                    ),
                    contents
                };
            }
        });
    });

    // Xcode-style Option+click (Alt+click elsewhere) — opens a persistent
    // popup with the same Quiver doc content the hover shows. Works even
    // if hover fails; useful for presenter mode where a lingering tooltip
    // is clearer than a hover that vanishes with the mouse.
    setupQuiverDocPopup(editor);

    // Initial cursor position render
    updateCursorPosition(1, 1);
});

/* --- Option+click doc popup (Xcode-style quick help) ---
 * Single-instance Monaco content widget. Alt/Option+clicking a word looks
 * it up in quiverDocs and pins a small panel below that word until the
 * user clicks outside or presses Escape. Keeping one widget at a time
 * avoids stacked ghosts; dismissing on outside-click matches VS Code and
 * Xcode muscle memory.
 */
let quiverDocWidget = null;

function closeQuiverDocPopup() {
    if (!quiverDocWidget || !editor) return;
    try { editor.removeContentWidget(quiverDocWidget); } catch (e) {}
    quiverDocWidget = null;
}

function openQuiverDocPopup(lineNumber, column, doc) {
    if (!editor) return;
    closeQuiverDocPopup();

    const node = document.createElement('div');
    node.className = 'quiver-doc-popup';
    if (doc.signature) {
        const sig = document.createElement('div');
        sig.className = 'quiver-doc-popup-signature';
        sig.textContent = doc.signature;
        node.appendChild(sig);
    }
    if (doc.summary) {
        const sum = document.createElement('div');
        sum.className = 'quiver-doc-popup-summary';
        sum.textContent = doc.summary;
        node.appendChild(sum);
    }
    // Swallow clicks inside the popup so the outside-click listener doesn't
    // immediately dismiss it on the same event that opened it.
    node.addEventListener('mousedown', (e) => e.stopPropagation());
    node.addEventListener('click', (e) => e.stopPropagation());

    quiverDocWidget = {
        getId: () => 'quiver.docPopup',
        getDomNode: () => node,
        getPosition: () => ({
            position: { lineNumber, column },
            preference: [
                monaco.editor.ContentWidgetPositionPreference.BELOW,
                monaco.editor.ContentWidgetPositionPreference.ABOVE,
            ],
        }),
    };
    editor.addContentWidget(quiverDocWidget);
}

function setupQuiverDocPopup(ed) {
    ed.onMouseDown((e) => {
        if (!e.event || e.event.altKey !== true) return;
        const position = e.target && e.target.position;
        if (!position) return;
        const model = ed.getModel();
        if (!model) return;
        const word = model.getWordAtPosition(position);
        if (!word) { closeQuiverDocPopup(); return; }
        const doc = quiverDocs[word.word];
        if (!doc) { closeQuiverDocPopup(); return; }
        // Block Monaco's default alt-click selection behavior so the popup
        // can open without the editor also dragging a box selection.
        if (e.event.preventDefault) e.event.preventDefault();
        openQuiverDocPopup(position.lineNumber, word.startColumn, doc);
    });

    // Dismiss on outside click. Popup swallows its own clicks (see above),
    // so any click that reaches document-level should close it.
    document.addEventListener('mousedown', () => {
        if (quiverDocWidget) closeQuiverDocPopup();
    });

    // Escape closes the popup anywhere — editor or document focus.
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && quiverDocWidget) closeQuiverDocPopup();
    });
}

